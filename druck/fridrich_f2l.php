Hier habt ihr eine Liste mit Algorithmen, welche euch dabei helfen k&ouml;nnten, die F2L's schneller zu l&ouml;sen!<br />
Ich empfehle es <b>nicht</b> diese Algorithmen auswendig zu lernen! Besser ist es immer, selbst und intuitiv eigene 'Algorithmen' zum L&ouml;sen der einzelnen F2L-F&auml;lle zu entwickeln.<br />
Wer trotzdem Vorschl&auml;ge braucht, hier meine Lerntipps:
<ul>
	<li>Schau nur nach einem Algorithmus, wenn du einen F2L Fall nicht oder zu langsam l&ouml;sen kannst!</li>
	<li>F&uuml;hr den Algorithmus aus und schau aber dabei genau, was du drehst</li>
	<li>Merk dir <b>nicht</b> die Algorithmen, sondern die Bewegungen und die 'Logik', warum diese Bewegungen zum Ziel f&uuml;hren!<br />
    => Dadurch kannst du wahrscheinlich auch gleich ein paar weitere F2L F&auml;lle schneller intuitiv l&ouml;sen.</li>
    <li>Verwende nicht immer die Ansicht, die hier gegeben ist! Manchmal kannst du z.B. den W&uuml;rfel zu Beginn nach links drehen (y) und dann werden alle F zu L, was wahrscheinlich schneller ausf&uuml;hrbar ist!</li>
    <li>Analysiere den Bewegungsablauf und zerlege ihn in Teile. Wenn ein Eck- oder Kantenstein mal im falschen Slot ist, dann musst du wissen, wie du vorgehst!</li>
    <li>Wenn du zuwenig Logik aus dem Algorithmus lernst, dann lern ihn lieber nicht, sondern l&ouml;se den Fall so, wie du es selbst machen w&uuml;rdest.</li>
</ul>
