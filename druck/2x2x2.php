<a name="step3"></a><br />
<h2>Schritt 3: Ecken der 2. Ebene ausrichten</h2>

<div style="float:right; padding-left:10px;"><img src="images/mini10.gif" alt="Symbol" border="0" /></div>
Um die Ecken der 2. Ebene auszurichten, braucht man nur einen einzigen Algorithmus! Dieser lautet R' D' R D und wird immer wieder wiederholt... Man geht so vor: halte den W&uuml;rfel so, dass eine Ecke, die gedreht werden muss, rechts/vorne/oben liegt. Dann f&uuml;hrst du den Algorithmus solange aus, bis nur diese Ecke richtig ist! Die 1. Ebene musst du dabei ignorieren, denn sie sieht nach diesem Algorithmus zerst&ouml;rt aus.<br /><br />

Nachdem die 1. Ecke richtig ist, drehst du nur die U-Ebene solange, bis die n&auml;chste falsch ausgerichtete Ecke rechts/vorne/oben ist und wendest den Algorithmus erneut an. Das ganze dann solange, bis der W&uuml;rfel gel&ouml;st ist!<br /><br />

