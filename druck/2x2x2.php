<h2>Schritt 1: Erste Ebene</h2>
<div style="float:right; padding-left:10px;"><img src="images/mini1.gif" alt="Symbol" border="0" /></div>
Eventuell kann man diesen Schritt schon ganz von alleine, aber ich werde ihn trotzdem nochmal erkl&auml;ren!<br />
Als Erstes sucht man sich irgendeine Ecke aus und baut dort eine gleichfarbige Ebene. Ich such mir immer gerne ein wei&szlig;es Eckst&uuml;ck, da ich mit wei&szlig; beginne.<br />
Links neben diese Ecke muss nun eine weitere Ecke, die die Farben wei&szlig; und gr&uuml;n hat und davon gibt es auch nurnoch eine! Wer die Ecke nun nicht alleine an den entsprechenden Platz bekommt, f&uuml;r den hier ein paar kleine Algorithmen (die bereits richtige Ecke wird immer hinten/links/oben gehalten):<br />

