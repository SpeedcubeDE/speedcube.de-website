Hier findet ihr eine Anleitung des sogenannten <b>2-Look OLL</b> der <a href="fridrich.php">Fridrich Methode</a>, das Kippen den Kanten- und Ecksteine der letzten Ebene in 2 Schritten.<br />
Die flachen gelben Fl&auml;chen an den Seiten stellen jene Stickerfl&auml;chen dar, die nach oben gedreht/gekippt werden m&uuml;ssen (mei&szlig;t gelb).<hr /><br /><br />

