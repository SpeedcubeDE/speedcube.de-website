<h2>6. Schritt: Ecken richtig positionieren</h2>
Jetzt musst du nur noch die Ecken l&ouml;sen. Dazu musst du sie erst mal an die richtige
Position bringen. Wie sie ineinander gedreht sind, spielt noch keine Rolle. Wichtig ist, dass
eine Ecke die Farben der 3 angrenzenden Mittelsteine hat, denn nur dann ist sie am
richtigen Platz!
Wenn noch keine Ecke an der richtigen Position ist, dann f&uuml;hre einen der folgenden
Algorithmen einfach einmal aus. Danach hast du mindestens eine richtig positionierte
Ecke.<br />
<small>(Das &quot;R&quot; steht f&uuml;r die
richtige Ecke)</small>

