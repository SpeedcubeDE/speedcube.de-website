<br /><br />
<h2>7. Schritt: Ecken richtig drehen</h2>
Wenn alle Ecken an der richtigen Position sind, dann musst du sie nur noch drehen, dann
ist der W&uuml;rfel fertig.
Mit dem folgenden Algorithmus drehst du die Ecken. Dazu musst du den W&uuml;rfel so halten,
dass die Ecke, die du drehen willst, vorne/rechts/oben ist.<br />
F&uuml;hre den Algorithmus so lange aus, bis die Ecke richtig ist. Dann drehst du nur (!) die gelbe
(obere) Ebene so lange, bis die n&auml;chste falsch gedrehte Ecke vorne/rechts/oben ist und f&uuml;hrst
den Algorithmus erneut aus, bis diese Ecke auch stimmt.
Danach wieder die gelbe Ebene drehen, bis die n&auml;chste falsch gedrehte Ecke rechts
vorne ist... Und so weiter, bis der W&uuml;rfel gel&ouml;st ist!<br />
<p><u>Anmerkung:</u> W&auml;hrend dieses Schrittes wird der W&uuml;rfel zwischendurch katastrophal aussehen,
lass dich dadurch aber nicht irritieren! Denn immer, wenn du den Algorithmus 6 mal
ausgef&uuml;hrt hast, wird der Rest wieder stimmen. Die Ecken lassen sich also mit 0, 6 oder
12 Ausf&uuml;hrungen des Algorithmus richtig drehen:</p>
