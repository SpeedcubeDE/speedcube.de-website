<tr><td><img border="0" src="images/lbl_tutorial6.gif" alt="Symbol" /></td>
  <td>U R U' R' U' F' U F</td></tr>
</table>

<p><u>Anmerkung:</u> Wenn sich in der oberen Ebene keine Kantensteine ohne gelb mehr befinden,
aber die 2. Ebene noch nicht gel&ouml;st ist, dann &quot;kipp&quot; irgendeinen Stein aus der gelben
Ebene mit einem der beiden Algorithmen dorthin, wo noch keine richtige, aber eine Kante
ohne gelb sitzt. (Danach hast du wieder eine Kante ohne gelb in der oberen Ebene)</p>

<br /><h2>4. Schritt: gelbes Kreuz</h2>

Nachdem du die 2. Ebene gel&ouml;st hast, musst du nun auf der letzten Ebene die 4
Kantensteine so kippen, dass ihre gelbe Stickerfl&auml;che nach oben zeigt. Konzentriere dich
auf die restlichen Kantensteine, es gibt vier F&auml;lle:

<ol>
  <li>Bei keinem Kantenstein ist die gelbe Stickerfl&auml;che oben</li>
  <li>Bei zwei Kantensteinen ist die gelbe Stickerfl&auml;che oben und sie sind &quot;um die Ecke&quot;
(90&deg; zueinander) angeordnet</li>
  <li>Bei zwei Kantensteinen ist die gelbe Stickerfl&auml;che oben und sie sind gegen&uuml;ber (&quot;in
einem Strich&quot;) angeordnet</li>
  <li>Bei allen Kantensteinen ist die gelbe Stickerfl&auml;che oben (fertig)</li>
</ol>
