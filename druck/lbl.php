<br /><h2>5. Schritt: Kantensteine des gelben Kreuzes richtig anordnen</h2>
Jetzt musst du die gelbe Ebene so drehen, dass zwei seitliche Stickerfl&auml;chen der Kanten
mit den Seiten (Mittelsteinen) deines W&uuml;rfels (rot, blau, orange oder gr&uuml;n)
&uuml;bereinstimmen.<br />
Wenn diese beiden Kanten &quot;um die Ecke&quot; angeordnet sind (also 90&deg; zueinander), dann
&uuml;berspringe den 1. Schritt, ansonsten f&uuml;hre beide aus:
(halte den W&uuml;rfel so, dass die richtigen Kantensteine so angeordnet sind, wie auf den
Bildern, also entweder vorne und hinten oder rechts und hinten)

