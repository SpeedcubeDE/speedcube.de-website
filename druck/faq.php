<b>Ich verstehe einige Fachbegriffe nicht.</b><br />
Schau dir mal die <a href="technical_terms.php">Fachbegriffe</a> an. Wenn du dort nicht f&uuml;ndig wirst, dann kannst du gerne im Forum nachfragen.<br /><br />

<a name="anker2"></a>
<b>Muss ich alle OLL's lernen?</b><br />
Nein, musst du nicht. Es gibt das sogenannte 2-Look OLL, in welchem du erst das gelbe Kreuz bildes, also die Kanten drehst, und nachher nurnoch die OLL-Algorithmen 4-10 brauchst.<br />
siehe auch <a href="fridrich_oll.php">Fridrich OLL</a><br /><br />

<a name="anker3"></a>
<b>Muss ich alle PLL's lernen?</b><br />
Du musst nicht alle PLL-Algorithmen lernen, um den Cube zu l&ouml;sen. Es gibt auch das sog. <a href="fridrich_2look_pll.php">2-Look PLL</a>, wobei du Ecken und Kanten der letzten Ebene in 2 Schritten l&ouml;st.<br /><br />

<a name="anker4"></a>
<b>Soll ich F2L-Algorithmen lernen?</b><br />
Nein, solltest du nicht. Mach alles, was du selber kannst auch selber. Die Algorithmen sind nur f&uuml;r jene, die selbst keine schnellere L&ouml;sung finden (bei manchen wenigen F&auml;llen findet man auch nur sehr schwer eine eigene L&ouml;sung). Also bitte nach M&ouml;glichkeit keine F2L Algorithmen lernen sondern die F2L intuitiv l&ouml;sen.<br /><br />

<a name="anker5"></a>
<b>Wo bekomme ich einen guten 3x3x3 Speedcube her?</b><br />
Gute Speedcubes sind z.B. DIY-Kit's bzw. der SCU von <a href="http://cubikon.de">Cubikon.de</a>, aber auch der sogenannte Diansheng Cube ist gut (zu Kaufen auf cube4you.com, siehe auch <a href="shops.php">Websites & Shops</a>).<br />
<b>Einfach mal die Seite <a href="cubes.php">Speedcubes</a> durchlesen.</b><br /><br />

<a name="anker6"></a>
<b>Wo bekomme ich einen guten 4x4x4 Speedcube her?</b><br />
Einer der besten 4x4 Cubes soll der von mefferts.com sein, aber auch der SCU4 von Cubikon.de (sogar mit dem von mefferts vergleichbar!) und die east-sheen Cubes sind sehr gut (zu Kaufen auf Cubikon.de, cube4you.com oder e-sheen.com, siehe auch <a href="shops.php">Websites & Shops</a>).<br /><br />

<a name="anker7"></a>
<b>Wo bekomme ich einen guten 2x2x2 Speedcube her?</b><br />
Kurze Antwort: Das groﬂe 2x2x2 Modell auf Cubikon.de f&uuml;r 8,95Ä ist ein guter 2x2 Cube. Auch der LanLan 2x2 Cube ist sehr beliebt (falls nicht sogar an Platz 1) und l&auml;sst sich in diversen Shops kaufen (siehe <a href="shops.php">Websites & Shops</a>)<br /><br />

<a name="anker8"></a>
<b>Wo bekomme ich gute 5x5x5, 6x6x6 und 7x7x7 Speedcubes her?</b><br />
Die besten Big Cubes sind die V-Cubes. Diese kann man auf Cubikon.de oder aber auch in anderen Shops wie z.B. cube4you.com (siehe auch <a href="shops.php">Websites & Shops</a>) bekommen. Website der V-Cubes ist v-cubes.com<br /><br />

