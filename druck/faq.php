<a name="anker10"></a>
<b>Darf bei einem Wettbewerb (WCA-Competition) jeder mitmachen oder muss man sich vorher anmelden?</b><br />
Bei Wettbewerben darf jeder mitmachen, aber man muss sich vorher anmelden. Dazu geht man auf die WCA-Seite des gew&uuml;nschten Wettbewerbs (z.B. links auf www.speedcubing.com Wettberwerb ausw&auml;hlen) und kann sich da zu einem bestimmten Zeitpunkt anmelden.!<br /><br />

<a name="anker11"></a>
<b>Wo sehe ich, wann Wettbewerbe stattfinden?</b><br />
Die wichtigsten Wettbewerbe werden im <a href="forum/" target="_blank">Forum</a> bekannt gegeben. Doch unter <a href="http://www.speedcubing.com">www.speedcubing.com</a> siehst du links immer eine Liste mit aktuellen Wettbewerben.<br /><br />

<a name="anker12"></a>
<b>Darf man sich den Cube bei einem Wettbewerb vorher anschauen?</b><br />
Man hat eine "inspection-time" von 15 Sekunden, man darf sich den Cube also bis zu 15 Sekunden anschauen, bis man den Timer gestartet haben muss. Wenn du bis 2 Sekunden l&auml; schaust, bekommst du eine +2 Strafe. Wenn du noch l&auml;nger schaust, ist der Solve ung&uuml;ltig<br /><br />

<a name="anker13"></a>
<b>Wo finde ich die ganzen Videos der Homepage auf Youtube?</b><br />
Speedcube.de hat einen eigenen Youtue-Channel Namens wwwspeedcubede, welchen man unter <a href="http://youtube.com/wwwspeedcubede">www.youtube.com/wwwspeedcubede</a> erreicht.<br />
Dort findet man Videos, die f&uuml;r die Website verwendet werden.<br /><br />

<a name="anker14"></a>
<b>Wie kann ich Videos, Tutorials oder anderes f&uuml;r die Homepage einreichen?</b><br />
Wenn du ein Video oder einen Text f&uuml;r Speedcube.de gemacht hast, dann wird dieser im <a href="forum/" target="_blank">Forum</a> gerne entgegengenommen! Einfach einen Thread unter "Vorschl&auml;ge" er&ouml;ffnen.
Das Video wird dann von mir auf dem wwwspeedcubede-Youtubechannel hochgeladen bzw. der Text wird von mir auf die Homepage gebracht und ich schreibe dazu, von welchem Forenmitglied es stammt.<br /><br />


