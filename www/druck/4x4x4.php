<h2>Schritt 1: Mitten l&ouml;sen</h2>
Im ersten Schritt l&ouml;st man alle Mitten, wobei man (wie beim 2x2x2 oder 6x6x6) die Problematik hat, dass es kein festes Mittelst&uuml;ck gibt. Entweder man wei&szlig; nun auswendig, welche Farbe wohin geh&ouml;rt, oder man benutzt die Ecken... im Bild rechts siehst du, dass die 3. Ebene (F) nur gr&uuml;n sein kann.<br /><br />

